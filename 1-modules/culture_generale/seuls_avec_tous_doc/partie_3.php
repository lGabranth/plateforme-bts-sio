	        <p>Le lien social c’est profondément transformé ces dernières années. Alors que les échanges s’intensifies, que nous communiquons désormais tous et tout le temps grâce à la mondialisation et au développement des nouvelles technologies, nous nous sentons paradoxalement de plus en plus isolés. Pour le professeur américain Brian Primack, « nous sommes des créatures fondamentalement sociales, mais la vie moderne à tendance à nous compartimenter plutôt que de nous rapprocher ».</p>
	        <p>Une grande partie de notre vie sociale semble depuis quelques années s’être déplacée du réel au virtuel. Le temps consacré aux réseaux sociaux et souvent mit en relation avec un sentiment de solitude et d’isolement. Nombreux sont ceux en effet qui passent davantage de temps sur internet que dans les rues, les restaurants, les lieux traditionnels de rencontre et de convivialité. </p> 
					<p>Est-ce un problème ? Faut-il s’en inquiéter, le déplorer, chercher à lutter contre ce nouvel état de fait, ou au contraire l’accepter ? </p>
					Pour certains en effet, loin de traduire un refus des relations humaines, la multiplication des interactions virtuelles doit être vue de manière positive. L’homme connecté cherche à créer du lien social, diversifie ses relations, s’ouvre davantage aux autres. Les moyens modernes de communication seraient donc une façon de rapprocher les individus.
					
					<ul class="mt-2">
						<li>Gilles LIPOVETSKY, <u>L’Ere du vide. Essai sur l’individualisme contemporain</u>, p.124</li>
						<li>Sherry TRUCKLE, <u>Seuls ensembles..</u>, p.129</li>
						<li>Charles BAUDELAIRE, <u>Le peintre de la vie moderne</u>, p.152</li>
						<li>Antonio CASILLI, <u>Les liaisons numériques : vers une nouvelle sociabilité ?</u>, p.156</li>
					</ul>
					<br>
					<h5 class="text-info">
						L’Ere du vide. Essai sur l’individualisme contemporain.
						<hr>
					</h5>
					<p>Le philosophe Gilles Lipovetsky affirme dans cet extrait que la société contemporaine repose sur une communication creuse, uniquement liée au besoin narcissique de séduire. Alors que la parole c’est démocratisé, ce qui devrait être une bonne chose, elle c’est en même temps vidé de sa substance. Tout le monde parle pour ne rien dire, pour le simple plaisir de s’écouter et d’imposer sa voix aux autres. Ces derniers d’ailleurs n’écoutent pas, étant eux-mêmes plus intéressés par leurs propres paroles. </p>
					<p>« Communiquer pour communiquer » sans se préoccuper du contenu, de celui à qui il est destiné, est un acte égoïste. Au lieu d’utiliser la parole pour échanger, pour créer un lien avec autrui, nous ne nous ne servons désormais plus que pour exister en tant qu’individu. </p>

					<br>

					<h5 class="text-info">
						Seuls ensembles..
						<hr>
					</h5>
					<p>L’anthropologue et psychanalyste américaine Sherry Truckle étudie les relations qui se développent entre les êtres humains et les ordinateurs. Pour elle, la proximité que nous avons développé avec cet outil nous isole, nous coupe des autres au lieu de nous en rapprocher. </p>
					<p>Par crainte de se retrouver exposés sur internet, d’être épiés, d’être jugés, nous finissons par ne plus oser être sincères. Les adolescents, familiers des nouvelles technologies, ont intégré cela. Certains jugent cette situation gênante mais tous semblent cependant accepter le fait d’être ou des voyeurs ou des exhibitionnistes. Ces progrès font ainsi de nous, de manière paradoxale, des êtres « seuls ensembles » enfermés dans leur bulle respective. </p>

					<br>

					<h5 class="text-info">
						Le peintre de la vie moderne.
						<hr>
					</h5>
					<p>Dans ce texte, Charles Baudelaire met en valeur la ville, lieu de rencontre et le fait de flâner, d’errer au hasard, librement, prêt à être surpris. Au milieu de la foule, de ses semblables, le promeneur averti peut beaucoup apprendre sur lui-même et sur les autres. Alors que les foules semblent parfois inquiétantes, donnent le sentiment de faire disparaître les individus, le poète les voit comme un moyen de s’intégrer aux autres sans pour autant perdre son identité. </p>
					<p>Au XXIème siècle, la flânerie se transpose sur la toile (internet). Lorsque Baudelaire écrit « l’observateur est un prince qui jouit partout de son incognito », il est difficile de ne pas faire le lien avec nos propres comportements en ligne. Surfer sur internet serait-il le pendant contemporain de flâner en ville au XIXème, un moyen d’être malgré tout en lien avec les autres ?</p>

					<br>

					<h5 class="text-info">
						Les liaisons numériques : vers une nouvelle sociabilité ?
						<hr>
					</h5>
					<p>Le sociologue Antonio Casilli s’intéresse à l’influence du numérique sur nos comportements. Il n’est à son avis pas pertinent de reprocher aux ordinateurs, à internet, aux réseaux sociaux d’avoir fait disparaître nos rapports sociaux. Considérer que le numérique nous désocialiserait relève d’un malentendu certain.  </p>
					<p>Pour Casilli, nous utilisons prioritairement les outils numériques pour entrer ou rester en contact avec d’autres individus. Internet et les réseaux sociaux illustrent notre besoin de cohésion. Grâce à eux, de nouvelles manières d’être ensemble émergent. Au lieu de nous isoler, de nous pousser à nous replier sur nous-même, internet nous emmène vers une nouvelle sociabilité, qui bien que différente n’en est pas moins réelle. </p>
