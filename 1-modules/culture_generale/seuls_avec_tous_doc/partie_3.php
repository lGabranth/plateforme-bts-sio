        Chapitre 2 : Réapprendre à vivre ensemble : renouer le lien social et s’ouvrir aux autres.
      </button>
    </h2>
  </div>
	<div class="row">
	  <div class="col">
	    <div class="collapse" id="chapitre2">
	      <div class="card card-body text-justify">
	        <p>L’expression « seuls avec tous » est en contradiction avec une autre formulation, plus souvent employée bien que très pessimiste, « Seul contre tous ». Nous sommes capables de créer des liens avec les autres, de ne pas les voir uniquement comme des ennemis, à condition de prendre conscience de ce qui nous rapproche, de nos désirs et de nos aspirations communes. Mais ce qui nous différencie ou nous éloigne à première vue des autres peut aussi être source de richesse, de découverte, de plaisir. </p>
	        <p>Vivre ensemble implique d’accepter ce que nous sommes et ce que sont ceux qui nous entourent. Être en désaccord avec les autres n’est pas nécessairement une mauvaise chose. Le groupe se fonde aussi sur la différence et prend tout son sens lorsqu’il respecte ce qui fait de nous des individus. S’écouter, chercher un terrain d’entente, dépasser les préjugés, accepter l’autre, même s’il est différent, permet de vivre harmonieusement ensemble et de renouer ce lien social que nos sociétés modernes malmènent parfois. </p> 
					
					<ul class="mt-2">
						<li>Antoine de SAINT-EXUPERY, <u>Terre des Hommes</u>, p.135</li>
						<li>Anna GAVALDA, <u>Ensemble, c'est tout</u>, p.137</li>
						<li>Jean-François MARMION, « s’occuper des autres, c’est s’occuper de soi », p.146</li>
					</ul>
					<br>
					<h5 class="text-info">
						Terre des hommes
						<hr>
					</h5>
					<p>Antoine de Saint-Exupéry, l’auteur du <u>Petit Prince</u> fut non seulement un immense écrivain mais aussi un très grand aviateur. Il a participé au développement de l’aviation civile et à l’ouverture de nouvelles voies aériennes, permettant ainsi de relier un peu plus les êtres humains tout autour de la terre. Ses œuvres valorisent autant l’individualisme, la solitude grandiose du pilote perdu en plein ciel, que la solidarité et le partage, valeur essentielle commune aux membre de l’Aéropostale. </p>
					<p>Dans ce passage de <u>Terre des hommes</u>, roman autobiographique, Saint-Exupéry souligne le fait que nos différences, si elles existent vraiment, devraient nous rapprocher au lieu de nous séparer. Les rivalités, l’émulation entre les hommes existe, mais ce qui doit demeurer en définitive, c’est la fraternité, le lien qui nous unit. Pour Saint-Exupéry nous sommes responsables les uns des autres et notre vie n’a de sens que lorsqu’elle s’inspire dans une perspective collective. </p>

					<br>

					<h5 class="text-info">
						Ensemble, C’est tout
						<hr>
					</h5>
					<p>Anna Gavalda met en scène dans ce roman quatre personnages qui partagent leur vie mais que tout oppose par ailleurs. Leur seul point commun, c’est le plaisir qu’ils éprouvent la plupart du temps à être ensemble. Ils ont conscience de leurs différences, mais doivent les accepter au lieu de les laisser les séparer. </p>
					<p>Camille, ex dessinatrice devenue femme de ménage, et Frank, cuisinier, reviennent dans ce passage sur ce qui les lie malgré tout. Frank doute de la possibilité de créer une relation lorsqu’on ne se ressemble pas, mais Camille est persuadée du contraire. Elle insiste sur ce que les autres et leurs différences peuvent nous apporter. </p>
					<p>Pour la jeune femme, ce qui compte c’est la volonté d’aller vers l’autre, quel qu’il soit. Ne fréquenter que des clones de nous-même, est-ce vraiment enrichissant ? </p>

					<br>

					<h5 class="text-info">
						« S’occuper des autres, c’est s’occuper de soi »
						<hr>
					</h5>
					<p>Dans ce texte, le psychologue Jean-François Marmion insiste sur le fait que l’épanouissement personnel, le bien-être de chacun, passe obligatoirement par l’engagement et le don de soi. Pour survivre, il est évidement fondamental de satisfaire les besoins vitaux, comme le fait de respirer, de dormir, de se nourrir… C’est ce que met en avant Abraham Maslow dans sa pyramide des besoins. Mais le bonheur repose aussi sur la relation que nous entretenons avec les autres, sur l’amour qu’ils nous donnent et l’estiment qu’ils nous accordent. Une vie sociale de qualité est indispensable à notre épanouissement, comme le montre l’importance qu’y accordent les adolescents. </p>
					<p>Nous avons besoin des autres, et aller vers eux nous est nécessaire à nous davantage encore qu’à eux. Plus qu’un acte désintéressé, être attentif aux autres relève d’un « altruisme utilitaire ». Les liens que nous tissons avec ceux qui nous entourent nous sont avant tout bénéfique. </p>
