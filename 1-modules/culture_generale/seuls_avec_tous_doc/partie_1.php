  </div>
	<div class="row">
	  <div class="col">
	    <div class="collapse" id="chapitre3">
	      <div class="card card-body text-justify">
	        <p>Même si l’on peut trouver des raisons de légitimer l’individualisme, il ne faut pas mésestimer les risques que présentent la glorification du moi. A force de ne nous préoccuper que de nous-même, quels que soient les raisons que nous nous donnons pour cela, nous risquons de finir par mépriser ou détester les autres et par nous montrer alors prêt à les écraser sans scrupules.</p>

	        <p>Les dérives individualistes aboutissent parfois à un véritable rejet des autres et mettent en péril la notion même de société. Comment, en effet, vivre ensemble si l’on refuse d’aller vers l’autre et que l’on se sent supérieur à lui ?</p> 

					<p>Par ailleurs, la solitude n’est pas toujours choisie et peut devenir une source de souffrance, comme le souligne Honoré de BALZAC lorsqu’il écrit : « La solitude est la souffrance multipliée par l’infini ». Lorsque nous sommes rejetés par la société, lorsque nous n’y trouvons plus notre place et que nous nous sentons condamné à être seul, que nous reste-t-il ?</p>
					
					<ul class="mt-2">
						<li>Molière, <u>Le misanthrope</u>, p.?</li>
						<li>Guy de MAUPASSANT, <u>Bel-Ami</u>, p.?</li>
						<li>Victor HUGO, <u>Les misérables</u>, p.?</li>
						<li>Luc PLAMANDON, "Le blues du businessman", p.?</li>
					</ul>
					<br>
					<h5 class="text-info">
						Le misanthrope.
						<hr>
					</h5>
					<p>Le héros de cette pièce de Molière, Alceste, déteste le genre humain et revendique sa misanthropie. Dans cet extrait, il explique à son ami Philinte ce qu’il reproche aux autres hommes et les raisons pour lesquelles ces derniers lui sont odieux. Il juge que certains de ces semblables sont mauvais par nature, et que les autres sont des lâches, méprisables parce qu’ils tolèrent le comportement des premiers. Philinte, plus compréhensif, ne nie pas les défauts de l’humanité puisqu’il rapproche l’attitude de l’être humain de celle des animaux sauvages comme le loup ou le vautour. Il se montre néanmoins tolérant et accepte les faiblesses de ses semblables, qui pour lui font partie de la nature humaine.</p>

					<p>L’attitude d’Alceste souligne les limites du repli sur soi : haïr ses semblables n’apporte pas le bonheur et ne permet pas de trouver le soutien dont on peut parfois avoir besoin.</p>

					<br>

					<h5 class="text-info">
						Bel-Ami.
						<hr>
					</h5>
					<p>Certains individus, totalement dépourvus de principes, sont tellement centrés sur eux-mêmes et persuadés de leur valeur qu’ils sont près à exploiter sans scrupules leurs semblables pour parvenir à leurs fins. C’est le cas de Georges Duroy, surnommé Bel-Ami, ce héros de Maupassant dont la seule ambition est de réussir et qui pour cela est prêt à piétiner ceux qui l’entourent.</p>
					<p>Égocentrique et arriviste, Duroy use et abuse de son charme sans prêter attention aux femmes et aux hommes qu’il blesse au passage par ses mensonges et ses tromperies. Le jour de son mariage, il n’a pas une pensée pour sa jeune épouse, seul son propre succès lui importe. Son attitude, bien qu’acceptée et même récompensée par la société, est moralement condamnable.</p>

					<br>

					<h5 class="text-info">
						Les Misérables.
						<hr>
					</h5>
					<p>Le héros des Misérables, Jean Valjean, est un homme pauvre, solitaire parce qu’il ne possède rien et parce que son caractère réservé l’isole du groupe. Réduit à voler un pain pour nourrir sa famille, il est arrêté et condamné aux travaux forcés..</p>
					<p>L’isolement de Jean Valjean est lié à l’origine à sa condition miséreuse. Cette solitude imposée a donc des causes sociales que dénonce Victor Hugo. A cette première exclusion s’ajoute le retranchement forcé de la société à la suite d’une injuste condamnation. Lorsqu’il est libéré après deux décennies de bagne, Valjean est traité comme un paria. Le voudrait-il qu’il lui serait devenu impossible de s’intégrer, le groupe le rejetant violemment à cause de son passé.</p>
					<p>La rédemption viendra pourtant des autres, Fantine, Cosette et plus particulièrement de Monseigneur Myriel, qui sera le premier à tendre la main à l’ancien forçat.</p>

					<br>

					<h5 class="text-info">
						"Le blues du businessman".
						<hr>
					</h5>
					<p>L’homme auquel Luc Plamandon donne la parole dans cette chanson est un homme solitaire. Il a certes réussi professionnellement, mais n’a pas réalisé ses rêves et le regrette. Sa réussite le coupe des autres, affadit ses relations avec les autres, les rend interchangeable à ses yeux. Il développe un comportement égocentrique et ramène tout à lui, comme le prouve l’emploi systématique du pronom « je ».</p>
					<p>Ici bien sûr, on ne peut pas réellement parler d’exclusion, la solitude de cet homme n’en est pas moins réelle et est pour lui une véritable cause de souffrance.</p>
