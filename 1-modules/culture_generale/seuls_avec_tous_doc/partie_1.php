	        <p>Lorsque le groupe met en place un système répressif, qui porte atteinte aux libertés individuelles ou qui uniformise les comportements, alors ce groupe peut être perçu comme un ennemi. Le système devient à ce moment-là synonyme d’enfermement et certains individus ne désirent plus qu’une chose, faire éclater ce cadre trop rigide. L’action individuelle devient le moyen de lutter contre les dérives du groupe.</p>

	        <p>Penser par soi-même donne l’occasion de s’affirmer et d’affirmer la valeur de l’individu, capable de se dresser contre des lois iniques (injustes). C’est ce que font des hommes et des femmes comme : </p> 

					<ul class="mt-2">
						<li>Jeanne d’Arc (p.1 du cahier photo), qui s’affranchie des limites imposées à son sexe par son époque.</li>
						<li>Emile Zola, qui se bat contre l’antisémitisme de la société française de l’époque. </li>
						<li>Malala Yousafzai, prix Nobel de la paix, qui lutte contre l’interdiction faite par les Talibans de scolariser les filles. Le combat solitaire peut ainsi être mené non pour sa propre gloire mais pour le bien de tous.</li>
					</ul>

					<p>Se retrouver seul face à tous, est-ce vraiment un mal ? Est-il parfois légitime de s’opposer au groupe ? D’aller contre les règles établies ? Quel est le prix à payer pour un tel comportement ?</p>
					
					<ul class="mt-2">
						<li>Albert CAMUS, <u>L'Etranger</u>, p.30</li>
						<li>SOPHOCLE, <u>Antigone</u>, p.36</li>
						<li>Boris Vian, "Le déserteur", p.38</li>
						<li>Thomas SCHAUDER, "Lanceurs d'alertes : désobéir, jusqu'où ?", p.43</li>
					</ul>
					<br>
					<h5 class="text-info">
						L'Etranger.
						<hr>
					</h5>
					<p>Le héros d’Albert CAMUS, Meursault, employé français installé en Algérie commet un meurtre. Jugé et condamné à mort, il est surtout victime de son refus de se plier aux conventions sociales, de s’intégrer au groupe. « Meursault est condamné parce qu’il ne joue pas le jeu. En ce sens, il est étranger à la société où il vit » écrit Albert CAMUS. Ce qui est reproché à cet homme, c’est moins le crime qu’il a commis que son refus d’obéir aux convenances, de faire preuve d’empathie, d’exprimer des remords.</p>
					<p>S’écarter des normes imposées par la majorité peut coûter très cher et faire de l’individu un paria, un étranger. </p>

					<br>

					<h5 class="text-info">
						Antigone.
						<hr>
					</h5>
					<p>Antigone, héroïne mythologique, refuses de se soumettre à une loi qu’elle juge injuste. Contestant la décision de son oncle Créon qui a interdit de faire inhumer le corps de son frère, considéré comme un traître, la jeune fille brave l’interdit et choisie la désobéissance. Elle paiera cette décision de sa vie.</p>
					<p>Le personnage d’Antigone frappe par son courage, par son mépris de la mort et par sa capacité à faire ce qui lui semble juste quelles qu’en soient les conséquences. Son sens moral est plus fort que son instinct de survie. </p>
					<p>Le personnage d’Antigone frappe par son courage, par son mépris de la mort et par sa capacité à faire ce qui lui semble juste quelles qu’en soient les conséquences. Son sens moral est plus fort que son instinct de survie. </p>

					<br>

					<h5 class="text-info">
						"Le Déserteur".
						<hr>
					</h5>
					<p>Le texte de Boris Vian, rédigé au moment de la guerre d’indépendance Algérienne, est un plaidoyer en faveur de la non-violence alors que les objecteurs de conscience étaient à cette époque considérés comme des déserteurs et donc, passible de condamnation, Boris Vian prend le risque de légitimé ce choix. L’écrivain détourne le terme de déserteur, qui donne son titre à la chanson. A l’origine péjoratif, le nom devient ici héroïque, le déserteur risquant sa vie au même titre que le soldat et l’acceptant pour rester en accord avec sa conscience et défendre son droit à ne pas tuer.</p>

					<br>

					<h5 class="text-info">
						"Lanceurs d’alerte : désobéir, jusqu’où ?".
						<hr>
					</h5>
					<p>Tout comme les objecteurs de conscience, les lanceurs d’alerte justifient leurs actes au nom de leurs principes, d’une morale plus forte que la loi, les règles ou la coutume. Face à des disfonctionnements sociaux, à des injustices, à des manquements au droit, ils estiment de leur responsabilité d’intervenir, quitte à commettre des actes illégaux.</p>
					<p>C’est le cas des militants de Greenpeace, arrêtés et condamnés pour s’être introduit illégalement dans une centrale nucléaire en octobre 2017. </p>
					<p>Cette désobéissance civile est-elle légitime ? Ne risque-t-elle pas de conduire vers l’anarchie ? Un Etat incapable de faire respecter les règles établies se retrouve en situation de faiblesse, ce qui explique l’attitude ambiguë des gouvernements face aux lanceurs d’alerte. Mais empêcher les individus d’agir selon leur conscience, est-ce vraiment juste ?</p>
