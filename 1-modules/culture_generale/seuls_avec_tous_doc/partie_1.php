	        <p>L’individu est, par la force des choses, au centre de notre pensée et de nos agissements. <br>
	        Nous percevons le monde et tout ce qui nous entoure par notre propre intermédiaire, nous sommes un avant d’être plusieurs.</p>
	        <p>« Connais-toi toi-même » était-il écrit dans l’antiquité sur le fronton du temple d’Apollon à Delphes. Cette injonction illustre l’idée qu’avant toute chose il est nécessaire de savoir qui l’ont est et donc légitime d’adopter un comportement individualiste.</p> 
					<p>Il est possible de considérer la solitude comme quelque-chose de positif, du moins lorsqu’elle est choisie et non subie. Cette solitude, loin d’être une preuve d’égoïsme, nous permet alors de nous retrouver, de faire le point sur ce que nous sommes, sur nos attentes et sur notre rapport aux autres. Pour mieux s’ouvrir à eux, il faut d’abord se consacrer à soi.</p>
					L’individualisme peut-il être vu comme une nécessité ? Est-il possible d’être heureux en étant seul ? S’isoler, est-ce se donner une chance de mieux se connaître ou bien seulement courir le risque de se couper durablement des autres ?
					
					<ul class="mt-2">
						<li>Michel de MONTAIGNE, <u>Essais</u>, p.16</li>
						<li>Sylvain TESSON, <u>Dans les forêts de Sibérie</u>, p.20</li>
						<li>Caspar David FRIEDRICH, <u>Le voyageur contemplant une mer de nuages</u>, p.26</li>
					</ul>
					<br>
					<h5 class="text-info">
						Essais.
						<hr>
					</h5>
					<p>Ce texte du philosophe Michel de MONTAIGNE, intitulé « De la solitude », met l’accent sur la singularité de l’individu et sur la nécessité d’apprendre à se connaître, à savoir qui nous sommes. Le philosophe insiste sur l’idée que la solitude est l’un des instruments permettant d’atteindre cette connaissance de soi, sans être influencé par le groupe.</p>
					<p>Montaigne note que les autres nous éloignent de nous-même, nous coupent de notre essence. Rechercher la solitude et se mettre volontairement à l’écart de la société n’est donc pas un acte égoïste. Au contraire, il s’agirait de la seule manière d’être libre et de trouver le bonheur y compris en revenant ensuite vers les autres.</p>
					<p>A l’inverse de Montaigne, un autre philosophe, Blaise PASCAL, affirme que « Le moi est haïssable ». Pour lui en effet, placer l’individu au centre de toute réflexion ne peut mener qu’au repli sur soi. Sans confrontation avec les autres, nos propres sentiments et nos émotions prennent le dessus abolissant tout recours au raisonnement et à la logique.</p>

					<br>

					<h5 class="text-info">
						Dans les forêts de Sibérie.
						<hr>
					</h5>
					<p>Le géographe Sylvain TESSON associe dans ce récit autobiographique l’aventure et le voyage intérieur. Le texte nous amène à prendre conscience, en même temps que le narrateur, de ce qu’est la solitude. Le départ du camion qui a amené Sylvain Tesson jusqu’au lieu de sa retraite, marque le début d’un isolement total. Ce départ est ressenti comme la marque d’une mise à l’écart, difficile à accepter bien que choisie.</p>
					<p>Le narrateur débute alors une quête initiatique, il s’interroge sur ce qui le relie aux autres, cherche à mieux se connaître, à se confronter à sa propre personnalité. La solitude lui permet de prendre conscience de ce qui l’entoure, des beautés du monde.</p>
					<p>« La présence des autres affadit le monde » constate-t-il ainsi dès l’instant où il se retrouve seul.</p>

					<br>

					<h5 class="text-info">
						Le voyageur contemplant une mer de nuage.
						<hr>
					</h5>
					<p>Le tableau de Caspar David FRIEDRICH est caractéristique du mouvement romantique. Ce dernier naît au XIXème siècle et se caractérise notamment par le triomphe de l’individuel sur le collectif. Les Confessions de Jean-Jacques Rousseau, prélude au romantisme, s’ouvre ainsi sur ses mots : « Moi seul ». L’homme et l’artiste se retrouvent seuls face à leur destin. « Le mal du siècle » cette insatisfaction et cette mélancolie permanente, illustre la tentation du repli sur soi et le questionnement devenu central sur l’individu.</p>
					<p>Dans le tableau de FRIEDRICH, la solitude semble choisie, volontaire. Le personnage peint dirige son regard vers la nature, vers l’horizon, vers l’immensité mystérieuse, et tourne le dos à la société et aux spectateurs. Le voyageur se trouve aussi au centre du tableau, ce qui est une manière symbolique d’en faire le centre du monde. L’œuvre de FRIEDRICH sublime la solitude et la quête de soi.</p>
