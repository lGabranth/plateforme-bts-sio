        Chapitre 3 : Libertés individuelles et pouvoir politique.
      </button>
    </h2>
  </div>
	<div class="row">
	  <div class="col">
	    <div class="collapse" id="chapitre3">
	      <div class="card card-body text-justify">
	        <p>Ces dernières années, dans les démocraties, c'est-à-dire dans les régimes politiques qui donnent le pouvoir au citoyen, les individus semblent se désintéresser petit à petit de la sphère publique. La participation aux élections baisse régulièrement, les mouvements conservateurs qui encouragent le repli sur soi rencontrent un succès grandissant. Derrière ces symptômes, ce qui est surtout remis en question, c’est une certaine uniformisation des modes de vies et la crainte d’une pensée dite « unique ». </p>
	        <p>Vivre dans une société régie par des lois et des règles semble nécessaire. L’union de tous empêche les individus de se dresser les uns contre les autres. La volonté générale l’emporte sur la volonté individuelle protégeant ainsi chacun d’entre nous. Mais se soumettre au groupe ne va pas forcément de soi et présente des dangers, le principal étant la perte de notre liberté. </p> 
					<p>Quels rapports entretiennent donc les individus et l’Etat, autorité qui représente le groupe ? Devons-nous sacrifier notre liberté à notre sécurité ? La liberté de chacun est-elle en contradiction avec la liberté de tous ? </p>
					
					<ul class="mt-2">
						<li>Alexis de TOCQUEVILLE, <u>De la démocratie en Amérique</u>, p.109</li>
						<li>George ORWELL, <u>1984</u>, p.117</li>
						<li>Hélène BALAZARD, « Des préoccupations quotidiennes à l’intérêt général », p.172</li>
					</ul>
					<br>
					<h5 class="text-info">
						De la démocratie en Amérique.
						<hr>
					</h5>
					<p>Dans <u>De la démocratie en Amérique</u>, ouvrage paru en 1835/1836, Alexis de TOCQUEVILLE s’intéresse à l’exemple des Etats-Unis, alors toute jeune nation. Il analyse le fonctionnement de la démocratie dans ce pays et en relève les risques. </p>
					<p>Désireuse d’instaurer une parfaite égalité entre tous les citoyens, les démocraties oublies parfois de prendre en compte les aspirations des individus. Elles se sentent parfaitement légitimes parce que leurs représentants sont élus par la majorité. Mais cela peut les amener à oppresser leurs minorités et à limiter les libertés de tous. D’autre part, pour permettre aux individus de vivre en collectivité, les régimes démocratiques encouragent plus ou moins consciemment le conformisme. Trouver un terrain d’entente commun au plus grand nombre est plus facile en uniformisant la pensée. La démocratie risque alors de faire disparaître l’esprit critique. </p>
					<p>Le danger majeur qui guette les sociétés démocratiques est le despotisme. Même si TOCQUEVILLE ne remet pas en question les avantages de la démocratie, vers laquelle selon lui tendent tous les peuples, il souligne aussi ses possibles dérives. Il affirme en particulier que « Le pouvoir de tout faire, que je refuse à un seul de mes semblables, je ne l’accorderai jamais à plusieurs ». </p>

					<br>

					<h5 class="text-info">
						1984.
						<hr>
					</h5>
					<p>Dans son roman d’anticipation publié en 1949, George Orwell mais en scène un monde dystopique dans lequel un parti unique, dirigé par « Big Brother » contrôle absolument tout. Les libertés individuelles ont disparu, les comportements autonomes sont sévèrement réprimés et seul le groupe est légitime. Penser par soi-même et refuser l’idéologie collective est devenu un crime, comme le héros, Winston, en fera l’amère expérience. </p>
					<p>George Orwell dénonce dans ce livre les dérives totalitaires, rendu possibles par la passivité du groupe. La toute-puissance de l’Etat, la violence, la surveillance et le contrôle de chacun par les autres donne naissance à une société cauchemardesque dans laquelle l’individu est broyé. Sous le prétexte d’assurer la sécurité de chacun, l’Etat, censé représenter le groupe, devient le pire des tyrans. </p>

					<br>

					<h5 class="text-info">
						« Des préoccupations quotidiennes à l’intérêt général »
						<hr>
					</h5>
					<p>Hélène Balazard s’intéresse aux différents moyens de faire évoluer les liens entre les individus et la société alors que la confiance dans les institutions politiques, qui devraient jouer ce rôle, diminue. Pour l’auteur, les intérêt particuliers et l’intérêt général ne sont pourtant pas en contradiction. En effet, c’est en défendant d’abord nous intérêts que nous sommes peu à peu amenés à nous préoccuper de l’intérêt général et à découvrir « le goût de faire ensemble ».</p>
					<p>Le pouvoir politique peut et doit encourager les initiatives personnelles, qui permettent à chacun de prendre conscience de sa capacité d’action. En s’impliquant, à son échelle, dans les affaires publiques, chaque citoyen exerce son pouvoir politique et contribue à faire respecter ses libertés individuelles. </p>
