      <button class="btn btn-link" type="button" data-toggle="collapse" data-target="#chapitre1" aria-expanded="true" aria-controls="collapseOne">
        Chapitre 1 : Mythes et philosophie : L’individu dans le groupe.
      </button>
    </h2>
  </div>
	<div class="row">
	  <div class="col">
	    <div class="collapse" id="chapitre1">
	      <div class="card card-body text-justify">
	        <p>L’article « Société » tiré de <u>L’Encyclopédie</u> rédigé au XVIIIème siècle sous la direction de Denis Diderot et de Jean LE ROND D’ALEMBERT commence par l’affirmation suivante : « L’homme est fait pour vivre en société ». La biologie paraît aller dans le sens de cette déclaration : Certaines parties du cerveau humain, celles qui régissent nos relations avec les autres, sont beaucoup plus développées que chez les animaux. Il semblerait donc que nous ayons besoin du groupe et que vivre seul ne nous soit pas naturel. </p>
	        <p>Pourtant, les penseurs et les philosophes se contredisent depuis longtemps sur la nature individualiste ou au contraire sociale de l’homme. De la même manière, les mythes antiques, reflets d’une époque et d’une certaine croyance, mettent en scène de façon parfois contradictoire des héros solitaires qui agissent pourtant souvent pour le groupe. </p> 
					<p>Le fonctionnement collectif est-il essentiel pour les individus ? La vie en société permet-elle de conserver sa liberté ?</p>
					A-t-on le droit de vouloir être seul lorsque tout semble affirmer que l’être humain est fait pour vivre avec les autres ?
					
					<ul class="mt-2">
						<li>Les Héros des mythes grecs (Achille et la guerre de Troie, Ulysse, Jason et les Argonautes)</li>
						<li>ARISTOTE, <u>Les Politiques</u>, p.77</li>
						<li>Jean-Jacques ROUSSEAU, Du contrat social, p.83</li>
						<li>Thomas HOBBES, <u>Léviathan</u>, p.80</li>
					</ul>
					<br>
					<h5 class="text-info">
						Les héros des mythes grecs.
						<hr>
					</h5>
					<p>Le héros grec est avant tout un être solitaire sur lequel repose des responsabilités importantes. Mais il est aussi intégré à la collectivité et œuvre en général pour le bien de son peuple. Ces personnages illustrent donc parfaitement les tensions qui existent entre les motivations de l’individu et celles du groupe. Achille, l’un des héros de la guerre de Troie, fait preuve d’un grand orgueil. En conflit avec Agamemnon, le chef des Grecs, il refuse de prendre part au combat malgré les pertes qui s’accumulent chez ses camarades. Lorsqu’il reprend finalement sa place pour venger son ami Patrocle et qu’il tue Hector, le prince Troyen traite la dépouille de ce dernier sans aucun respect, aveuglé par la colère et le chagrin. Achille est à la fois un gros héros, associé à un combat collectif, et un homme seul face à son destin.</p>
					<p>Après la guerre de Troie, Ulysse cherche à rentrer chez lui, à Ithaque pour y retrouver sa femme Pénélope et leur fils Télémaque. Son retour est retardé par Poséidon, le Dieu des Mers, qui lui en veut d’avoir blessé son fils, le cyclope Polyphème. Bien qu’accompagné de tout un équipage, Ulysse affronte souvent seul les différents dangers. Son équipage lui sert davantage de faire-valoir que de véritable soutien. Il n’y a d’ailleurs que lui qui arrivera à rentrer sain et sauf, après avoir perdu en route tous ses compagnons. </p>
					<p>Jason, descendant du dieu Eole, monte une expédition pour rapporter de Colchide la Toison d’Or. Il fait construire un bateau baptisé « l’Argo » sur lequel il s’embarque avec 50 jeunes héros. Parmi ces Argonautes (passagers de l’Argo) se trouvent Hercule (ou Héraclès), Thésée, Castor et Pollux, Orphée, Atalante.. Tous puisent dans les ressources de chacun pour former un groupe uni et efficace. L’un construit le bateau, l’autre le pilote. Jason mène l’expédition, Orphée l’accompagne en musique. On peut voir ce mythe comme une métaphore de la place de l’individu dans le groupe. </p>

					<br>

					<h5 class="text-info">
						Les Politiques.
						<hr>
					</h5>
					<p>L’être humain est pour Aristote naturellement sociable. Le philosophe grec qualifie l’homme d’animal politique, qui recherche le bien commun et l’intérêt général. Il est donc normal pour ce dernier de se rapprocher des autres, de se constituer en société permettant à chacun de bien vivre et non plus de seulement survivre. </p>
					<p>Celui qui vit hors de la citée, qui se détache du groupe, est donc pour Aristote soit un surhomme, un héros, soit au contraire un barbare, presque un animal. L’homme ne peut s’accomplir qu’en groupe et s’en exclure lui est néfaste, à moins qu’il ne soit très différent du reste de l’humanité. </p>
					<p>Ce besoin de vivre en collectivité se manifeste par la maitrise du langage, qui n’est pas juste un moyen de communication mais aussi le moyen d’argumenter et de partager des valeurs morales. </p>

					<br>

					<h5 class="text-info">
						Du contrat social.
						<hr>
					</h5>
					<p>Jean-Jacques ROUSSEAU estime qu’à « l’Etat de Nature », c'est-à-dire à l’origine, avant de se constituer en société, l’homme est bon. Mais dès que des interactions sociales interviennent, c’est la loi du plus fort qui l’emporte, ce qui n’est pas acceptable. Il est donc nécessaire de mettre en place un système qui protège chaque individu. </p>
					<p>Pour cela, les hommes se lient les uns aux autres et acceptent volontairement de se soumettre au groupe. C’est ce que ROUSSEAU appelle le contrat social. Chaque individu se place librement sous la domination de l’ensemble et renonce en partie à sa liberté, au profit du groupe, à la condition que les autres en fassent autant. Le contrat social doit permettre de préserver autant que possible les libertés individuelles. </p>
					<p>Les sociétés ainsi constituées courent malgré tout le risque de priver l’homme de son indépendance et de le corrompre. Même nécessaire, le contrat social est donc très difficile à mettre en place. </p>

					<br>

					<h5 class="text-info">
						Léviathan.
						<hr>
					</h5>
					<p>Seul un pouvoir commun est capable de protéger les individus contre un Etat de Nature, qui entraîne nécessairement la guerre puisque selon Thomas HOBBES, « l’homme est un loup pour l’homme ».  Pour ne pas se détruire mutuellement, les êtres humains doivent donc volontairement renoncer à leurs droits et libertés, se soumettre à des lois et s’en remettre au pouvoir d’un seul homme ou d’une assemblée malgré la contrainte que cela représente. </p>
					<p>Cette forme de pacte social, lorsqu’elle est légitimement et unanimement acceptée assure la paix et la sécurité de chacun. Cela permet aussi de transformer une multitude, un ensemble d’individus aux motivations divergentes, en un groupe uni. L’énorme pouvoir que les individus accordent à l’autorité politique est une manière de préserver le bien-être de tous.</p>
