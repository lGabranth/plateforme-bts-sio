	    <div class="collapse" id="chapitre2">
	      <div class="card card-body text-justify">
	        <p>Le sentiment d’appartenance à une communauté pousse les individus à agir, à s’affranchir de leur tendance à l’individualisme pour s’associer aux autres. L’action groupée peut être très positive, permettre des avancées sociales, assurer la défense des libertés fondamentales, causer le renversement de tyrans.. Ce qui semblait impossible aux citoyens isolés le devient pour le groupe, dont la voix démultipliée et amplifiée se fait alors bien plus clairement entendre. L’enthousiasme, la solidarité, l’union autour d’un chef charismatique galvanise le groupe, surtout lorsqu’il est persuadé de la justesse de son combat. </p>
	        <p>Mais l’action collective peut aussi présenter des dangers. En s’associant aux autres, l’individu semble parfois abandonner toute capacité de réflexion et perdre son sens critique. L’instinct grégaire nous pousse à imiter nos semblables, et ce même si un tel comportement risque de se retourner contre nous.</p> 
					<p>Faut-il nécessairement agir comme les autres ? Réalisons-nous toujours dans quelle direction nous entraîne le groupe ? Avons-nous vraiment la capacité de lui résister ? </p>
					
					<ul class="mt-2">
						<li>Eugène DELACROIX, <u>La Liberté guidant le peuple </u></li>
						<li>Emile ZOLA, <u>Germinal</u>, p.89</li>
						<li>Gustave LE BON, <u>Psychologie des foules</u>, p.101</li>
						<li>René GIRARD, <u>Le Bouc Emissaire</u>, p.103</li>
					</ul>
					<br>
					<h5 class="text-info">
						La liberté guidant le peuple.
						<hr>
					</h5>
					<p>Il s’agit d’un tableau du peintre Eugène DELACROIX, réalisé en 1830 et inspiré de la révolution des Trois Glorieuses ; qui renverse le roi Charles X. La dimension allégorique de ce tableau et son aspect politique en ont fait un symbole de la république française et de la démocratie. </p>
					<p>Au centre du tableau, la figure de la liberté, coiffée d’un bonnet phrygien et tenant le drapeau français mène les combattants à la victoire. Héroïque, engagée, elle rassemble le peuple, unissant autour d’elle toutes les catégories sociales, l’ouvrier, l’étudiant, le gamin parisien. </p>
					<p>Une cause juste permet aux individus de se lier, de former un groupe soudé et les pousse vers l’action collective, même si cela doit leur coûter la vie. </p>

					<br>

					<h5 class="text-info">
						Germinal.
						<hr>
					</h5>
					<p>Le roman Germinal, œuvre d’Emile Zola met en scène des mineurs qui luttent pour améliorer leurs conditions de travail. La naissance du syndicalisme permet à ces travailleurs, jusqu’alors anonymes, isolés et méprisés, de faire entendre leur voix en se révoltant contre la manière dont ils sont traités par les exploitants des mines. </p>
					<p>Dans cet extrait, le syndicaliste Etienne LANTIER incite les mineurs à se battre contre ceux qui les exploitent. Seul face à la foule qu’il cherche à convaincre, Lantier remporte l’adhésion du groupe, il peut dès lors compter sur son soutien. </p>
					<p>Emile Zola montre ici de quelle manière l’union du groupe peut se faire autour d’un seul homme, lorsqu’il trouve les bons mots. Il laisse cependant aussi entendre que la foule est une entité dangereuse et versatile qui peut basculer sans prévenir dans la violence. </p>
					<p>Même légitime, la contestation peut ouvrir la voie à des actions brutales. Changeant, le groupe peut à tout moment se radicaliser. </p>

					<br>

					<h5 class="text-info">
						Psychologie des foules.
						<hr>
					</h5>
					<p>Dans ce texte, Gustave Le Bon se penche sur le mode de fonctionnement de la foule. Cette dernière, lorsqu’elle prend une forme organisée, n’est pas une simple juxtaposition d’individus : Elle provoque un mécanisme d’identification et donc entraîne et désinhibe ceux qui la compose. Le comportement de la foule n’est absolument pas identique à celui des particuliers, ceux-ci agissant de manière totalement différente lorsqu’ils sont seuls et lorsqu’ils font partie d’un ensemble. </p>
					<p>Dans le groupe, les personnalités individuelles ont tendances à s’évanouir au profit d’une identité collective. Chacun agit instinctivement comme son voisin. Ce mimétisme s’apparente parfois à de l’aveuglement et à un refus de se remettre en question. Mais la foule peut aussi, de manière bien plus positive, refléter « l’âme d’un peuple », sa véritable nature. </p>

					<br>

					<h5 class="text-info">
						Le Bouc Emissaire.
						<hr>
					</h5>
					<p>Le Bouc Emissaire est une victime symbolique qui incarne tout ce que le groupe déteste, ce dont il veut se débarrasser ou se venger. L’académicien René Girard explique ainsi les persécutions de masse, qui sont selon lui d’avantage le fait d’un groupe que celui d’individus. La foule se cherche un coupable, sans ne se préoccuper ni de la vérité ni de la justice, et agit férocement une fois son coupable trouvé. </p>
					<p>La violence est ici le résultat d’un emballement mimétique, les mauvais comportements des uns entraînant ceux des autres. En groupe, les hommes s’abandonnent plus facilement à leurs pulsions, à leurs instincts destructeurs, parce que la caution des autres leur donnent l’impression que ce qu’ils font est légitime. Cet appétit de violence, ce goût du sang, soude même plus sûrement le groupe que n’importe quoi d’autre et pousse les individus qui le compose à abandonner leur rationalité et leur esprit critique.</p>
