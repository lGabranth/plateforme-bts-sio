	        <p>L’individu est, par la force des choses, au centre de notre pensée et de nos agissements. <br>
	        Nous percevons le monde et tout ce qui nous entoure par notre propre intermédiaire, nous sommes un avant d’être plusieurs.</p>
	        <p>« Connais-toi toi-même » était-il écrit dans l’antiquité sur le fronton du temple d’Apollon à Delphes. Cette injonction illustre l’idée qu’avant toute chose il est nécessaire de savoir qui l’ont est et donc légitime d’adopter un comportement individualiste.</p> 
					<p>Il est possible de considérer la solitude comme quelque-chose de positif, du moins lorsqu’elle est choisie et non subie. Cette solitude, loin d’être une preuve d’égoïsme, nous permet alors de nous retrouver, de faire le point sur ce que nous sommes, sur nos attentes et sur notre rapport aux autres. Pour mieux s’ouvrir à eux, il faut d’abord se consacrer à soi.</p>
					L’individualisme peut-il être vu comme une nécessité ? Est-il possible d’être heureux en étant seul ? S’isoler, est-ce se donner une chance de mieux se connaître ou bien seulement courir le risque de se couper durablement des autres ?
					
